                        <h3>台灣首個導管瓣膜團隊，罕見跨科合作開刀</h3>
                        <p>隨著台灣邁向超高齡社會，因老化而引起主動脈瓣膜狹窄的患者也大幅增加。台北榮總心臟內科指出，大部份病人在主動脈瓣膜狹窄初期幾乎無感，無法早期察覺出身體上的異狀，一旦症狀出現，通常已到疾病末期，所以預後情況也非常差，想要有效治療嚴重主動脈瓣膜狹窄，手術治療是唯一選擇。</p>
                        <p>心臟主動脈瓣膜是左心室與主動脈之間的重要開關，如果主動脈瓣膜不能正常打開或閉鎖不全，從左心室流向主動脈的血流就會改變，以致於部份血液流不出去、影響全身血液循環，增加心臟輸送血液的負擔，長期下來可能會引發胸悶、胸痛，嚴重者還會出現心絞痛、昏厥、心臟衰竭等症狀。</p>
                        <img src="styles/images/new/2.jpg" alt="張效煌醫師官方網站">
                        <h3>台北榮總由心臟外科和內科醫師組成的「導管瓣膜團隊」，創下術後一年存活率達百分之87紀錄</h3>
                        <p>台北榮總心臟內科「導管瓣膜」團隊，日前收治80歲患者唐媽媽，她在72歲時罹患主動脈瓣膜狹窄合併升主動脈瘤，由北榮心臟外科張效煌醫師評估施行傳統開心手術，置換主動脈瓣膜，同時進行升主動脈修補，術後復原良好。但近期因走路、爬樓梯會喘再度就醫，經檢查發現第一次置換植入的豬瓣膜因退化造成狹窄，需再次手術。張醫師考量唐媽媽已屆高齡，建議施行「瓣中瓣手術」。</p>
                        <p>院方表示，這是升級版的經導管主動脈瓣膜置換手術（TAVI），就是利用導管將新的支架瓣膜放置在出問題的瓣膜處，撐開舊瓣膜開口，以「瓣中瓣」技術，讓瓣膜得以正常運作。</p>
