                        <h3>關於心臟衰竭</h3>
                        <p>心臟衰竭是指心臟功能失常，無法輸出足量的血液去維持身體的新陳代謝，影響全身血液循環。長期下來不僅會讓心臟負擔過重，致使心臟產生病變，也會因各重要器官、細胞無法得到足量的營養及氧氣，繼而引起其他合併症出現。</p>
                        <h3>致病原因</h3>
                        <p>許多疾病都會導致心臟衰竭，大致上可略分為以下兩類：</p>
                        <ul>
                            <li>一、 非心臟疾病：代謝症候群、高血壓、糖尿病、長期貧血、甲狀腺機能亢進、酗酒、 服用心臟毒性藥物、病毒感染、懷孕等。</li>
                            <li>二、 與心臟疾病相關：心臟瓣膜狹窄或閉鎖不全、冠狀動脈疾病、心房顫動、風濕性心臟病、心肌病變、先天性心臟病等。</li>
                        </ul>
                        <h3>心臟衰竭的類型</h3>
                        <p>許多疾病都會導致心臟衰竭，大致上可略分為以下兩類：</p>
                        <h5>一、左心衰竭</h5>
                        <p>左心室負責體循環，當左心室因輸出血液功能出現問題，以致血液滯留於左心房和肺靜脈，長期下來會使左心室負荷過重、造成結構改變（如左心室肥大），導致左心衰竭。左心衰竭，又分成：</p>
                        <h5>收縮型心臟衰竭(HFrEF)</h5>
                        <p>指左心室收縮能力下降，無力打出血液。</p>
                        <h4>舒張型心衰竭（HFpEF）</h4>
                        <p>左心室無法正常放鬆，使血液難以完全流回心臟。</p>
                        <h4>二、右心衰竭</h4>
                        <p>右心室負責肺循環，血液經靜脈至右心房返回到右心室，然後右心室將血液輸送至肺部以補充氧氣；當右側功能時，血液會回流到身體的靜脈中而無法到肺部做氣體交換，長期下來會造成右心室肥大，導致右心衰竭。</p>
                        <p>右心衰竭在臨床上通常是左心衰竭的結果。 當左心室衰竭時，肺部微血管因壓力升高，使右心後負荷增加，最終損害心臟右側；也有一些病患是因肺高壓、先天性心臟病、冠狀動脈疾病等而引起右心衰竭。</p>
                        <h3>心臟衰竭的四個階段</h3>
                        <p>美國紐約心臟協會（NYHA）依據病患活動能力受限程度將心臟衰竭分為四級。</p>
                        <img src="styles/images/edu/1.jpg" alt="張效煌醫師官方網站">
                        <h3>心臟衰竭的徵兆與防治</h3>
                        <p>心臟衰竭的徵兆包括：(1)容易疲累、運動與活動能力降低。(2)端坐呼吸，夜間陣發性呼吸。(3)雙腳浮腫、尿量減少。(4)呼吸困難，會喘、咳嗽。(5)四肢冰冷發紫。(6)肝腫大、腹痛。(7)食慾不振。(8)意識改變，甚至昏迷。</p>
                        <p>其他如焦慮、不安、記憶力受損、惡夢、失眠或頭暈等也都是臨床上會出現的症狀。</p>
                        <p>要防治心臟衰竭惡化可從以下幾點做起：(1)臥床休息。(2)按時服藥。(3)水份需限量飲用。(4)控制體重。(5)注意小便量。(6)遠離煙酒。(7)定期門診檢查。(8)保持心情愉快。</p>
                        <h3>簡單的自我照顧評估表</h3>
                        <img src="styles/images/edu/2.jpg" alt="張效煌醫師官方網站">
                        <h3>治療方法</h3>
                        <h5>一、藥物治療： </h5>
                        <p>視病況合併多種藥物來控制病情，目的為減少心臟負擔。常用藥物有血管張力素轉化抑制劑 (Angiotensin converting enzyme inhibitor, ACEI)、血管張力素受器阻斷 (Angiotensin receptor blocker, ARB)、乙型交感神經阻斷劑 (Beta-blocker )、腎上腺皮質酮拮抗劑(Mineralocortocoid receptor antagonist, MRA)、血管收縮素受體中性溶酶抑制劑(Angiotensin receptor neprilysin inhibitor, ARNI)、If離子電流選擇性抑制劑 (I f inhibitor)、利尿劑 (Diuretic)、毛地黃(Digoxin)等。</p>
                        <h5>二、侵入性治療：</h5>
                        <p>在藥物無法改善病情的情況下，會依病情緊急程度選擇不同侵入性治療方案，如植入心臟節律器、心臟瓣膜置換手術(經導管進行主動脈瓣膜植入術)、心導管或冠狀動脈繞道手術、葉克膜、主動脈內氣球幫浦 （intra-aortic balloon pump，IABP）、人工心臟（心室輔助器）、心臟移植等。</p>
